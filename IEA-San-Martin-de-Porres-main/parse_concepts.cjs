const items = [];
let currentCode = null;
let currentText = '';

for (const line of lines) {
  const match = line.match(/^(C\d{3})\s*(.*)$/);
  if (match) {
    if (currentCode) {
      items.push({ code: currentCode, text: currentText.trim().replace(/\s+/g, ' ') });
    }
    currentCode = match[1];
    currentText = match[2];
  } else if (currentCode) {
    currentText += ' ' + line.trim();
  }
}

